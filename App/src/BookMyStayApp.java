/**
 * Hotel Booking Management System
 *
 * This class represents the entry point of the Book My Stay application.
 * It demonstrates how a Java program starts execution and prints
 * a welcome message to the console.
 *
 * The program simply displays the application name and version
 * to confirm that the system has started successfully.
 *
 * @author Student
 * @version 1.0
 */
public class BookMyStayApp {

    /**
     * Main method – entry point of the Java application.
     * The JVM invokes this method when the program starts.
     *
     * @param args command line arguments
     */
    public static void main(String[] args) {

        // Print welcome message
        System.out.println("=================================");
        System.out.println(" Welcome to Book My Stay ");
        System.out.println(" Hotel Booking System v1.0 ");
        System.out.println("=================================");

        // Application started successfully
        System.out.println("Application started successfully.");
    }
}